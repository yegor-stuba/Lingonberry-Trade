// Printer.java
class Printer {
    // Overloaded method for printing a string
    public void print(String text) {
        System.out.println("Entry is the String type with the value of: " + text);
    }

    // Overloaded method for printing an integer
    public void print(int number) {
        System.out.println("Entry is the int type with the value of: " + number);
    }
}
